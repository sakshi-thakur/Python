str = "Hello World !!!"
print (str)                       #prints complete string
print (str[0])                    #prints first character of the string
print (str[2:5])                  #prints character starting from 3rd to 5th
print (str[2: ])                  #prints character starting from 3rd
print (str * 2)                   #prints string two times
print (str + "Welcome")           #concatenate strings
